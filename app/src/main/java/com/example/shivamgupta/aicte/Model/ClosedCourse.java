package com.example.shivamgupta.aicte.Model;

public class ClosedCourse {

    private String aicteId;

    public String getAicteId() { return this.aicteId; }

    public void setAicteId(String aicteId) { this.aicteId = aicteId; }

    private String instituteName;

    public String getInstituteName() { return this.instituteName; }

    public void setInstituteName(String instituteName) { this.instituteName = instituteName; }

    private String instituteType;

    public String getInstituteType() { return this.instituteType; }

    public void setInstituteType(String instituteType) { this.instituteType = instituteType; }

    private String state;

    public String getState() { return this.state; }

    public void setState(String state) { this.state = state; }

    private String district;

    public String getDistrict() { return this.district; }

    public void setDistrict(String district) { this.district = district; }

    private String courseId;

    public String getCourseId() { return this.courseId; }

    public void setCourseId(String courseId) { this.courseId = courseId; }

    private String university;

    public String getUniversity() { return this.university; }

    public void setUniversity(String university) { this.university = university; }

    private String level;

    public String getLevel() { return this.level; }

    public void setLevel(String level) { this.level = level; }

    private String course;

    public String getCourse() { return this.course; }

    public void setCourse(String course) { this.course = course; }

    private String shift;

    public String getShift() { return this.shift; }

    public void setShift(String shift) { this.shift = shift; }

    private String timing;

    public String getTiming() { return this.timing; }

    public void setTiming(String timing) { this.timing = timing; }

    private String year;

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }
}
