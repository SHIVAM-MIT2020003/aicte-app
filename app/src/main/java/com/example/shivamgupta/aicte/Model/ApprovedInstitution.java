package com.example.shivamgupta.aicte.Model;


public class ApprovedInstitution {
    private String aicteId;

    public String getAicteId() { return this.aicteId; }

    public void setAicteId(String aicteId) { this.aicteId = aicteId; }

    private String name;

    public String getName() { return this.name; }

    public void setName(String name) { this.name = name; }

    private String address;

    public String getAddress() { return this.address; }

    public void setAddress(String address) { this.address = address; }

    private String district;

    public String getDistrict() { return this.district; }

    public void setDistrict(String district) { this.district = district; }

    private String institutionType;

    public String getInstitutionType() { return this.institutionType; }

    public void setInstitutionType(String institutionType) { this.institutionType = institutionType; }

    private String women;

    public String getWomen() { return this.women; }

    public void setWomen(String women) { this.women = women; }

    private String minority;

    public String getMinority() {
        return minority;
    }

    public void setMinority(String minority) {
        this.minority = minority;
    }
}
