package com.example.shivamgupta.aicte.Model;

public class ClosedInstitute
{
    private String aicteID;

    public String getAicteID() { return this.aicteID; }

    public void setAicteID(String aicteID) { this.aicteID = aicteID; }

    private String instituteName;

    public String getInstituteName() { return this.instituteName; }

    public void setInstituteName(String instituteName) { this.instituteName = instituteName; }

    private String instituteType;

    public String getInstituteType() { return this.instituteType; }

    public void setInstituteType(String instituteType) { this.instituteType = instituteType; }

    private String address;

    public String getAddress() { return this.address; }

    public void setAddress(String address) { this.address = address; }

    private String state;

    public String getState() { return this.state; }

    public void setState(String state) { this.state = state; }

    private String district;

    public String getDistrict() { return this.district; }

    public void setDistrict(String district) { this.district = district; }

    private String city;

    public String getCity() { return this.city; }

    public void setCity(String city) { this.city = city; }

    private String year;

    public String getYear() { return this.year; }

    public void setYear(String year) { this.year = year; }
}
